export function resolveInitialLanguage() {
  try {
    const saved = localStorage.getItem(LANG_KEY);
    if (SUPPORTED_LANGUAGES.includes(saved)) return saved;
  } catch {}
  return DEFAULT_LANGUAGE;
}

export function makeT(lang) {
  return (key) => {
    const parts = key.split('.');
    let node = translations[lang] || translations.ko;
    for (const part of parts) {
      node = node?.[part];
      if (node == null) break;
    }
    if (typeof node === 'string') return node;

    let fallback = translations.ko;
    for (const part of parts) fallback = fallback?.[part];
    return typeof fallback === 'string' ? fallback : key;
  };
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(resolveInitialLanguage);

  useEffect(() => {
    try {
      localStorage.setItem(LANG_KEY, lang);
      document.documentElement.lang = lang;
    } catch {}
  }, [lang]);

  useEffect(() => {
    const onStorage = (e) => {
      if (e.key === LANG_KEY && SUPPORTED_LANGUAGES.includes(e.newValue)) {
        setLang(e.newValue);
      }
    };
    window.addEventListener('storage', onStorage);
    return () => window.removeEventListener('storage', onStorage);
  }, []);

  const setLanguage = useCallback((nextLang) => {
    setLang(SUPPORTED_LANGUAGES.includes(nextLang) ? nextLang : DEFAULT_LANGUAGE);
  }, []);

  const toggleLanguage = useCallback(() => {
    setLang(prev => (prev === 'ko' ? 'en' : 'ko'));
  }, []);

  const t = useMemo(() => makeT(lang), [lang]);
  const value = useMemo(() => ({
    lang,
    language: lang,
    setLanguage,
    toggleLanguage,
    t,
  }), [lang, setLanguage, toggleLanguage, t]);

  return createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}
